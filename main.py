"""1. Egyszerű számbekérő
Kérj be egy számot a felhasználótól, és döntsd el, hogy páros vagy páratlan. Írd ki az eredményt!”””
"""

number = int(input("Adj meg egy számot! "))

if number % 2 == 0:
    print(f"Ez a szám páros! ({number})")
elif number % 2 != 0:
    print(f"Ez a szám páratlan! ({number})")
else:
    print("Kérlek adj meg egy valid számot! ")